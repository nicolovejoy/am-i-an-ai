import React from 'react';

type ResultType = 'human' | 'ai' | 'unknown';

interface ResultsProps {
  result: ResultType | null;
  confidence?: number;
  isLoading: boolean;
}

const Results: React.FC<ResultsProps> = ({ result, confidence = 0, isLoading }) => {
  if (isLoading) {
    return (
      <div className="mt-8 terminal">
        <div className="flex flex-col items-center justify-center py-8">
          <div className="sci-fi-loading"></div>
          <p className="mt-6 text-lg text-neon-blue font-mono typing-cursor">
            Running analysis sequence...
          </p>
          <div className="mt-4 w-full max-w-md bg-dark-blue bg-opacity-50 p-2 font-mono text-xs">
            <p className="text-neon-blue">$ initiating neural pattern recognition</p>
            <p className="text-neon-purple">$ scanning linguistic structures</p>
            <p className="text-terminal-green">$ analyzing semantic coherence</p>
            <p className="text-neon-pink typing-cursor">$ processing statistical markers</p>
          </div>
        </div>
      </div>
    );
  }

  if (!result) {
    return null;
  }

  const getResultData = () => {
    switch (result) {
      case 'human':
        return {
          title: 'Human-Written Text Detected',
          description: 'Analysis indicates this text was likely created by a human author.',
          color: 'green',
          icon: 'üë§',
          className: 'result-human',
        };
      case 'ai':
        return {
          title: 'AI-Generated Text Detected',
          description:
            'Analysis indicates this text was likely generated by an artificial intelligence.',
          color: 'blue',
          icon: 'ü§ñ',
          className: 'result-ai',
        };
      case 'unknown':
      default:
        return {
          title: 'Analysis Inconclusive',
          description: 'Cannot confidently determine if this text was written by human or AI.',
          color: 'pink',
          icon: '‚ùì',
          className: 'result-unknown',
        };
    }
  };

  const resultData = getResultData();

  return (
    <div className="mt-8 terminal">
      <div className={`p-6 ${resultData.className}`}>
        <div className="flex items-start">
          <span className="text-4xl mr-4">{resultData.icon}</span>
          <div>
            <h3 className="text-xl font-bold neon-text font-mono tracking-wide uppercase">
              {resultData.title}
            </h3>
            <p className="mt-2 text-gray-300">{resultData.description}</p>
            {confidence > 0 && (
              <div className="mt-4">
                <div className="flex justify-between mb-1">
                  <p className="text-sm font-medium font-mono">
                    Confidence Rating:{' '}
                    <span className="text-neon-blue">{Math.round(confidence)}%</span>
                  </p>
                  <p className="text-xs text-gray-400 font-mono">System certainty</p>
                </div>
                <div className="w-full bg-dark-blue rounded-full h-2.5 mb-4 relative neon-border">
                  <div
                    className={`h-2.5 rounded-full ${
                      result === 'human'
                        ? 'bg-terminal-green'
                        : result === 'ai'
                          ? 'bg-neon-purple'
                          : 'bg-neon-pink'
                    }`}
                    style={{ width: `${confidence}%` }}
                  ></div>
                </div>
              </div>
            )}

            <div className="mt-5 p-3 bg-dark-blue bg-opacity-70 border border-gray-700 rounded">
              <h4 className="text-sm uppercase tracking-wider font-mono text-gray-400 mb-2">
                Analysis Notes
              </h4>
              <ul className="text-sm space-y-1">
                {result === 'human' ? (
                  <>
                    <li className="text-terminal-green">‚úì Natural language variations detected</li>
                    <li className="text-terminal-green">
                      ‚úì Inconsistent pattern usage consistent with human writing
                    </li>
                    <li className="text-terminal-green">
                      ‚úì Organic thought progression identified
                    </li>
                  </>
                ) : result === 'ai' ? (
                  <>
                    <li className="text-neon-purple">
                      ‚úì Statistical patterns match known AI signatures
                    </li>
                    <li className="text-neon-purple">
                      ‚úì Consistent structural elements identified
                    </li>
                    <li className="text-neon-purple">‚úì Predictable language patterns detected</li>
                  </>
                ) : (
                  <>
                    <li className="text-neon-pink">‚ö† Mixed indicators present</li>
                    <li className="text-neon-pink">
                      ‚ö† Insufficient data for confident classification
                    </li>
                    <li className="text-neon-pink">‚ö† Recommend additional sampling</li>
                  </>
                )}
              </ul>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Results;
